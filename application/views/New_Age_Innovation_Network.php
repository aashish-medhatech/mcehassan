                   <p class="text-justify">An expo was organized where teams were asked to present their ideas and models regarding real-world problems. The expo was held on the 16th of August in the CS seminar hall conducted by TCE with the guidance and presence of Dr. A. Geetha Kiran, HoD of the CS Department and co-ordinator of ME-RIISE.</p>
                   <p class="text-justify">It was divided into two domains: software and hardware. The teams who were participating in software problems were earlier trained in various software applications. Teams needed to prepare the prototypes or apps using these applications and present them at the expo. Teams with hardware problems were provided with various kinds of machines such as 3D printers, laser cutters and a tinkering lab. Teams were asked to prepare the working model by using these.</p>
                   <p class="text-justify">The expo was held in two rounds. In the first round, all the registered teams were allowed to present their model to the jury consisting of Mrs. Prakruthi H L, Assistant Professor EC department and Mr. Aruna Kumar, Associate Professor CS department, Dr. Anjan Sriranga, Professor at HIMS, Dr. A. Geetha Kiran, Coordinator of ME-RIISE and HOD CS department and Mrs. Chaithra, Assistant Professor EI department. And after the evaluation, the top 10 teams were selected for the second round. In the second round, the teams were again evaluated by the jury panel headed by Dr. Anjan Sriranga. Students from A.V.K college and Holly Mother School also visited the expo. Finally, at the end of the event, the best 3 teams were awarded cash prizes and participation certificates were issued to all the participants.</p>
                   
